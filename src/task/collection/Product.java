public class Product {


    private int id;
    private String name;
    public int quantity;

    public Product(int id, String name, int quantity) {
        this.id = id;
        this.name = name;
        this.quantity = quantity;
    }

    @Override
    public String toString() {
        return "Product{" +
                "id = " + id + '\'' +
                ", name = " + name +
                ", quantity = " + quantity +
                '}';
    }

}


